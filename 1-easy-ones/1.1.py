# Take two inputs from the user. One will be an integer. The other will be a float number. Then multiply them to display the output.


# Take integer as string format, then cast it to integer
integer = int(input("Give me one integer: "))
# integer = int(integer_text)

# Take float number as string format and cast it to float datatype
float = float(input("Give me one float: "))

multiply = integer * float
print(multiply)